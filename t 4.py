# O(n)
def sumBase(n, k):
    a = ''                  # счётчик-строка
    while n > 0:            # пока число больше нуля
        a += str(n % k)     # к счетчику прибавляем остаток от деления нацело
        n = n // k          # n равно целой части от деления
    suma = 0
    for i in a:             # проходимся по каждому элементу a
        suma += int(i)      # сумму преобразуем из строки в целое число
    return suma             # возвращаем сумму